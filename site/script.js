const blockedShortcuts = new Set(["c", "x", "s", "p", "u", "a"]);
const encodedContact = [114, 97, 121, 46, 49, 57, 55, 57, 64, 103, 109, 97, 105, 108, 46, 99, 111, 109];

function getContactAddress() {
  return String.fromCharCode(...encodedContact);
}

function getContactScheme() {
  return ["mai", "lto", ":"].join("");
}

function revealContactAddress(event) {
  event.preventDefault();
  const address = getContactAddress();
  const href = `${getContactScheme()}${address}`;
  document.querySelectorAll("[data-email-link]").forEach((link) => {
    link.href = href;
    link.setAttribute("aria-label", `Email ${address}`);
    const value = link.querySelector("[data-email-value]");
    if (value) value.textContent = address;
  });
  window.location.href = href;
}

