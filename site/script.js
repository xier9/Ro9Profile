function setLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-Hant" : "en";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[language][key]) {
      element.textContent = translations[language][key];
    }
  });
  languageToggle.textContent = language === "zh" ? "中" : "EN";
  languageToggle.setAttribute("aria-label", language === "zh" ? "切換英文" : "Switch to Chinese");
  localStorage.setItem("roger-profile-language", language);
  updateSkillDetail(document.querySelector(".skill-pill.active")?.dataset.skill || "strategy");
}

function updateSkillDetail(skill) {
  const detail = skillDetails[currentLanguage][skill];
  skillDetail.innerHTML = `<h3>${detail.title}</h3><p>${detail.text}</p>`;
}

languageToggle.addEventListener("click", () => {
  setLanguage(currentLanguage === "zh" ? "en" : "zh");
});

themeToggle.addEventListener("click", () => {
  const isDark = document.documentElement.dataset.theme === "dark";
  document.documentElement.dataset.theme = isDark ? "light" : "dark";
  themeToggle.textContent = isDark ? "☾" : "☀";
  localStorage.setItem("roger-profile-theme", isDark ? "light" : "dark");
});

menuButton.addEventListener("click", () => {
  const expanded = menuButton.getAttribute("aria-expanded") === "true";
  menuButton.setAttribute("aria-expanded", String(!expanded));
  navLinks.classList.toggle("open", !expanded);
  document.body.classList.toggle("menu-open", !expanded);
});

navLinks.addEventListener("click", (event) => {
  if (event.target.tagName === "A") {
    menuButton.setAttribute("aria-expanded", "false");
    navLinks.classList.remove("open");
    document.body.classList.remove("menu-open");
  }
});

